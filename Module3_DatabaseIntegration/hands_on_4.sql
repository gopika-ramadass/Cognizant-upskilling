/* 48. EXPLAIN Query */

EXPLAIN
SELECT
    s.first_name,
    s.last_name,
    c.course_name
FROM enrollments e
JOIN students s
ON s.student_id = e.student_id
JOIN courses c
ON c.course_id = e.course_id
WHERE s.enrollment_year = 2022;


/*
49. Query Plan Analysis

Before creating indexes, MySQL/PostgreSQL may perform
a Full Table Scan (MySQL) or Sequential Scan (PostgreSQL)
on students, enrollments, and courses.

This is acceptable for small tables but becomes slow
for large datasets.
*/


