/* 
   TASK 3 : N+1 PROBLEM DOCUMENTATION
*/


/*
56. N+1 Problem Example

Python Logic:

SELECT * FROM enrollments;

FOR EACH enrollment:
    SELECT first_name,last_name
    FROM students
    WHERE student_id = ?


If there are 12 enrollments:

1 query to fetch enrollments
+
12 queries to fetch students

Total = 13 queries

This is called N+1 Query Problem.
*/


/*
57. Optimized Solution

Use a single JOIN query instead.
*/

SELECT
    e.enrollment_id,
    CONCAT(s.first_name,' ',s.last_name) AS student_name,
    c.course_name,
    e.grade
FROM enrollments e
JOIN students s
ON e.student_id = s.student_id
JOIN courses c
ON e.course_id = c.course_id;


/*
58. Comparison

N+1 Version:
------------
13 database round trips

JOIN Version:
-------------
1 database round trip

Both produce identical results.

JOIN version is significantly faster.
*/


/*
59. Real World Scenario

If there are 10,000 enrollments:

N+1 Version:
------------
1 + 10,000
=
10,001 Queries

JOIN Version:
-------------
1 Query

Extra Queries Issued:
---------------------
10,000 additional queries

Hence JOIN / eager loading should always
be preferred in large applications.
*/

